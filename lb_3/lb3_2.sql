SELECT Track.track_title, Artist.artist_name
FROM Playlist_track
JOIN Track ON Playlist_track.Track_track_id = Track.track_id
JOIN Playlist ON Playlist_track.Playlist_playslist_id = Playlist.playslist_id
JOIN Artist_track ON Track.track_id = Artist_track.Track_track_id
JOIN Artist ON Artist_track.Artist_artist_id = Artist.artist_id
WHERE Playlist.User1_user_id = 1

-- Задание 2 Найти 5 самых популярных исполнителей (по общему количеству треков в плейлистах пользователей).
SELECT 
    Artist.artist_name,
    COUNT(Playlist_track.Track_track_id) AS track_count
FROM Artist
JOIN Artist_track ON Artist.artist_id = Artist_track.Artist_artist_id
JOIN Playlist_track ON Artist_track.Track_track_id = Playlist_track.Track_track_id
GROUP BY Artist.artist_id, Artist.artist_name
ORDER BY track_count DESC
LIMIT 5;
